const fs = require('fs');
const path = require('path');

// Create a minimal valid PNG (1x1 transparent pixel)
const minimalPng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x20,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x73, 0x7A, 0x7A,
  0xF4, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
  0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
  0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00,
  0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE,
  0x42, 0x60, 0x82
]);

const iconsDir = path.join(__dirname, 'src-tauri', 'icons');
if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// Create icon.png
fs.writeFileSync(path.join(iconsDir, 'icon.png'), minimalPng);
console.log('Created icon.png');

// Create 32x32.png
fs.writeFileSync(path.join(iconsDir, '32x32.png'), minimalPng);
console.log('Created 32x32.png');

// Create 128x128.png
fs.writeFileSync(path.join(iconsDir, '128x128.png'), minimalPng);
console.log('Created 128x128.png');

// Create 128x128@2x.png
fs.writeFileSync(path.join(iconsDir, '128x128@2x.png'), minimalPng);
console.log('Created 128x128@2x.png');

console.log('All icons created successfully!');
